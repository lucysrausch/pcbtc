/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART1_UART_Init();
  MX_USART2_UART_Init();
  MX_TIM17_Init();
  /* USER CODE BEGIN 2 */

  //USB-MIDI Init
  MX_USB_MIDI_INIT();

  //Init misc
  ledInit();

  if(FUNC_ERROR == midiInit() ){
	  while(1){
		  HAL_GPIO_WritePin(LED_POW_GPIO, LED_POW_PIN, SET);
		  HAL_Delay(500);
		  HAL_GPIO_WritePin(LED_POW_GPIO, LED_POW_PIN, RESET);
		  HAL_Delay(500);
	  }
  }

  //Wait usb configuration.
  while(1){
	  if(USBD_STATE_CONFIGURED == hUsbDeviceFS.dev_state){
		  HAL_GPIO_WritePin(LED_POW_GPIO, LED_POW_PIN, SET);
		  break;
	  }else{
		  HAL_GPIO_WritePin(LED_POW_GPIO, LED_POW_PIN, RESET);
	  }
  }

  //start led-blinking timer
  HAL_TIM_Base_Start_IT(&htim17);

  //Start MIDI Receive from IN Jack
  HAL_UART_Receive_DMA(&huart1, &uart_rx_dat[0],1);
  HAL_UART_Receive_DMA(&huart2, &uart_rx_dat[1],1);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

  //Wait USB configuration when USB connection error has occurred.
	  while(1){
		  if(USBD_STATE_CONFIGURED == hUsbDeviceFS.dev_state){
			  HAL_GPIO_WritePin(LED_POW_GPIO, LED_POW_PIN, SET);
			  break;
		  }else{
			  HAL_GPIO_WritePin(LED_POW_GPIO, LED_POW_PIN, SET);
			  HAL_Delay(200);
			  HAL_GPIO_WritePin(LED_POW_GPIO, LED_POW_PIN, RESET);
			  HAL_Delay(200);
		  }
	  }

	//[USB-MIDI IN] to [MIDI JACK OUT]
	for(uint32_t cable_num=0; cable_num<MIDI_OUT_JACK_NUM; cable_num++){

		if(isUartReady[cable_num]){
			if( FUNC_SUCCESS == midiGetFromUsbRx(cable_num, &uart_tx_dat) ){
				sendUart(cable_num, &uart_tx_dat);
			}
		}
	}

	//[MIDI JACK IN] to [USB-MIDI OUT]
	midiProcess();

	ledMain();

  }
  /* USER CODE END 3 */

}

